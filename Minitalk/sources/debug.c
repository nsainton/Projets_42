#include "minitalk.h"

void	print_bits(t_byte byte)
{
	int unsigned i;

	i = 0;
	while (i < 8)
	{
		if (byte & 1 << i)
			write(1, "1", 1);
		else
			write(1, "0", 1);
		i ++;
	}
	write(1, "\n", 1);
}
